//STEP 1
function halfNumber(n) {
    let divided = n/2
    console.log(`Half of ${n} is ${divided}.`)
    return divided
} 

//STEP 2
function squareNumber(n) {
    let squared = n**2
    console.log(`The result of squaring the number ${n} is ${squared}.`)
    return squared
} 

