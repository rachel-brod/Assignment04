function percentOf(dividend, divisor) {
        let percent = (dividend/divisor)*100
        console.log(`${dividend} is ${percent} of ${divisor}.`)
        return percent
} 

//STEP 4
function findModulus(dividend, divisor) {
    let modulus = (dividend%divisor)
    console.log(`${modulus} is the modulus of ${dividend} and ${divisor}.`)
}

